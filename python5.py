#Kisaca Metotlar
mesaj="mutlu ol"
print(mesaj.capitalize()) #capitalize() metodu:Karakter dizinin ilk harfini buyuk harf yapar.
print(mesaj.title())      #title() metodu:Karakter dizisinin icindeki tum kelimelerin ilk harfini buyuk yapar.
print(mesaj.upper())      #upper() metodu:Tum harfleri buyuk harfe cevirir.
print(mesaj.lower())      #lower() metodu:Tum harfleri kucuk harfe cevirir.
print(mesaj.replace('mutlu','kendin'))  #replace: ilk kelime ile ikinci kelimenin yerini degistirir.
